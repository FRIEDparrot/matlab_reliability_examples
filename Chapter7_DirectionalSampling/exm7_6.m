%% %%%%%%%%%%%%%%%%% 7.6 相关变量的求解 %%%%%%%%%%%%%%%%%%%%%% 
mu_ = [2, 3, 10];
sigma_d = [1, 5, 2];
rho_ = [1,  0.5, 0.6; 0.5, 1, 0.2; 0.6, 0.2, 1];
sigma_ = sigma_d' * sigma_d .* rho_;

g = @(x) x(:,1) + 2 * x(:,2) - x(:,3)  + 5;

%% %%%%%%%%% 最简调用只需使用这个即可 %%%%%%%%%%%%%%%%%%%%%% 
num_DS = 3e3;
[Pf] = DS_solu(mu_, sigma_, {g}, num_DS);
[Pf2] = MCS_solu(mu_,sigma_, g);
